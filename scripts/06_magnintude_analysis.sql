ORDER BY total_boxes_shipped DESC;


-- Find marketing spend by sales channel.
SELECT
    Channel,
    ROUND(SUM(Marketing_Spend),2) AS total_marketing_spend
FROM orders
GROUP BY Channel
ORDER BY total_marketing_spend DESC;


-- Analyze salesperson performance.
-- Find total revenue generated by each salesperson.
SELECT
    Salesperson,
    ROUND(SUM(Amount),2) AS total_revenue,
    ROUND(
        SUM(Amount) * 100.0 / SUM(SUM(Amount)) OVER(),
        2
    ) AS revenue_percentage
FROM orders
GROUP BY Salesperson
ORDER BY total_revenue DESC;

-- Find total orders handled by each salesperson.
SELECT
    Salesperson,
    COUNT(Order_ID) AS total_orders
FROM orders
GROUP BY Salesperson
ORDER BY total_orders DESC;
